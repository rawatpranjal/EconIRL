#!/usr/bin/env python3
"""TD-CCP Primer — auto-generate results for the LaTeX document.

Runs TD-CCP (linear semi-gradient) and NFXP on the Rust bus engine,
writes LaTeX macros and a results table to td_ccp_results.tex.

This reproduces the core experiment from Adusumilli & Eckardt (2025):
transition-free estimation on a tabular MDP with known ground truth.

Usage:
    cd /path/to/econirl
    .venv/bin/python papers/econirl_package/primers/td_ccp/td_ccp_results.py
"""

import json
import time
from pathlib import Path

import jax.numpy as jnp
import numpy as np

OUT = Path(__file__).resolve().parent / "td_ccp_results.tex"

N_BINS = 90
BETA = 0.9999
TRUE_OC = 0.001
TRUE_RC = 3.0
N_INDIVIDUALS = 500
N_PERIODS = 100
SEED = 42


def main():
    from econirl.environments.rust_bus import RustBusEnvironment
    from econirl.estimation.td_ccp import TDCCPEstimator, TDCCPConfig
    from econirl.estimation.nfxp import NFXPEstimator
    from econirl.preferences.linear import LinearUtility

    n_obs = N_INDIVIDUALS * N_PERIODS
    print(f"TD-CCP Primer — generating results")
    print(f"  Environment: Rust bus, {N_BINS} bins, beta={BETA}")
    print(f"  Data: {N_INDIVIDUALS} x {N_PERIODS} = {n_obs:,} obs")

    env = RustBusEnvironment(
        operating_cost=TRUE_OC, replacement_cost=TRUE_RC,
        num_mileage_bins=N_BINS, discount_factor=BETA,
    )
    panel = env.generate_panel(n_individuals=N_INDIVIDUALS, n_periods=N_PERIODS, seed=SEED)
    utility = LinearUtility.from_environment(env)
    problem = env.problem_spec
    transitions = env.transition_matrices

    results = {}

    # ── NFXP ──
    print("\n  Running NFXP...")
    t0 = time.time()
    nfxp = NFXPEstimator(
        optimizer="BHHH", inner_solver="policy",
        inner_tol=1e-12, inner_max_iter=200,
        se_method="robust", verbose=False,
    )
    nfxp_r = nfxp.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    nfxp_time = time.time() - t0
    results["nfxp"] = {
        "oc": float(nfxp_r.parameters[0]), "rc": float(nfxp_r.parameters[1]),
        "ll": float(nfxp_r.log_likelihood), "time": nfxp_time,
        "se_oc": float(nfxp_r.standard_errors[0]) if nfxp_r.standard_errors is not None else None,
        "se_rc": float(nfxp_r.standard_errors[1]) if nfxp_r.standard_errors is not None else None,
    }
    print(f"    oc={results['nfxp']['oc']:.6f}, rc={results['nfxp']['rc']:.4f}, "
          f"LL={results['nfxp']['ll']:.2f}, time={nfxp_time:.1f}s")

    # ── TD-CCP (linear semi-gradient) ──
    print("\n  Running TD-CCP (semi-gradient)...")
    t0 = time.time()
    tdccp = TDCCPEstimator(config=TDCCPConfig(
        method="semigradient", basis_dim=4,
        cross_fitting=True, robust_se=True,
        n_policy_iterations=1, verbose=False,
    ))
    tdccp_r = tdccp.estimate(panel=panel, utility=utility, problem=problem, transitions=transitions)
    tdccp_time = time.time() - t0
    results["tdccp"] = {
        "oc": float(tdccp_r.parameters[0]), "rc": float(tdccp_r.parameters[1]),
        "ll": float(tdccp_r.log_likelihood), "time": tdccp_time,
        "se_oc": float(tdccp_r.standard_errors[0]) if tdccp_r.standard_errors is not None else None,
        "se_rc": float(tdccp_r.standard_errors[1]) if tdccp_r.standard_errors is not None else None,
    }
    print(f"    oc={results['tdccp']['oc']:.6f}, rc={results['tdccp']['rc']:.4f}, "
          f"LL={results['tdccp']['ll']:.2f}, time={tdccp_time:.1f}s")

    # ── Write LaTeX ──
    def fmt(v, d=6):
        return f"{v:.{d}f}" if v is not None else "---"

    tex = []
    tex.append("% Auto-generated by td_ccp_results.py — do not edit by hand")
    tex.append(f"% Rust bus, {N_BINS} bins, beta={BETA}, {n_obs} obs, seed={SEED}")
    tex.append("")
    tex.append(f"\\newcommand{{\\tdNbins}}{{{N_BINS}}}")
    tex.append(f"\\newcommand{{\\tdBeta}}{{{BETA}}}")
    tex.append(f"\\newcommand{{\\tdNobs}}{{{n_obs:,}}}")
    tex.append(f"\\newcommand{{\\tdTrueOC}}{{{TRUE_OC}}}")
    tex.append(f"\\newcommand{{\\tdTrueRC}}{{{TRUE_RC}}}")
    tex.append("")
    # NFXP
    tex.append(f"\\newcommand{{\\tdNfxpOC}}{{{fmt(results['nfxp']['oc'])}}}")
    tex.append(f"\\newcommand{{\\tdNfxpRC}}{{{fmt(results['nfxp']['rc'], 4)}}}")
    tex.append(f"\\newcommand{{\\tdNfxpLL}}{{{fmt(results['nfxp']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\tdNfxpTime}}{{{fmt(results['nfxp']['time'], 1)}}}")
    tex.append(f"\\newcommand{{\\tdNfxpSEoc}}{{{fmt(results['nfxp']['se_oc'])}}}")
    tex.append(f"\\newcommand{{\\tdNfxpSErc}}{{{fmt(results['nfxp']['se_rc'])}}}")
    tex.append("")
    # TD-CCP
    tex.append(f"\\newcommand{{\\tdOC}}{{{fmt(results['tdccp']['oc'])}}}")
    tex.append(f"\\newcommand{{\\tdRC}}{{{fmt(results['tdccp']['rc'], 4)}}}")
    tex.append(f"\\newcommand{{\\tdLL}}{{{fmt(results['tdccp']['ll'], 2)}}}")
    tex.append(f"\\newcommand{{\\tdTime}}{{{fmt(results['tdccp']['time'], 1)}}}")
    tex.append(f"\\newcommand{{\\tdSEoc}}{{{fmt(results['tdccp']['se_oc'])}}}")
    tex.append(f"\\newcommand{{\\tdSErc}}{{{fmt(results['tdccp']['se_rc'])}}}")
    tex.append("")

    # Table
    tex.append("\\begin{table}[h!]")
    tex.append("\\centering\\small")
    tex.append(f"\\caption{{TD-CCP (semi-gradient, $K\\!=\\!4$ basis) versus NFXP on Rust bus "
               f"(\\tdNbins\\ states, $\\beta = \\tdBeta$, \\tdNobs\\ obs). "
               f"True: $\\theta_c = \\tdTrueOC$, $RC = \\tdTrueRC$.}}")
    tex.append("\\label{tab:tdccp_results}")
    tex.append("\\begin{tabular*}{\\textwidth}{@{\\extracolsep{\\fill}} l r r}")
    tex.append("\\toprule")
    tex.append("Metric & NFXP & TD-CCP \\\\")
    tex.append("\\midrule")
    tex.append("$\\hat\\theta_c$ & \\tdNfxpOC & \\tdOC \\\\")
    tex.append("$\\hat{RC}$ & \\tdNfxpRC & \\tdRC \\\\")
    tex.append("SE($\\theta_c$) & \\tdNfxpSEoc & \\tdSEoc \\\\")
    tex.append("SE($RC$) & \\tdNfxpSErc & \\tdSErc \\\\")
    tex.append("Log-likelihood & $\\tdNfxpLL$ & $\\tdLL$ \\\\")
    tex.append("Time (s) & \\tdNfxpTime & \\tdTime \\\\")
    tex.append("\\bottomrule")
    tex.append("\\end{tabular*}")
    tex.append("\\end{table}")

    OUT.write_text("\n".join(tex) + "\n")
    print(f"\n  Wrote {OUT}")
    OUT.with_suffix(".json").write_text(json.dumps(results, indent=2))
    print(f"  Wrote {OUT.with_suffix('.json')}")


if __name__ == "__main__":
    main()
